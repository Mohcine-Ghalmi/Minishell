#include "libft.h"

char	*ft_strchr(const char *s, int c)
{
	int		i;
	char	find;

	if (!s)
		NULL;
	find = (char)c;
	i = 0;
	while (s[i])
	{
		if (s[i] == find)
			return ((char *)s + i);
		i++;
	}
	if (s[i] == find)
		return ((char *)s + i);
	return (NULL);
}
