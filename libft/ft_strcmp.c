#include "libft.h"

int	ft_strcmp(char *s1, char *s2)
{
	int	i;

	i = 0;
	if (ft_strlen1(s1) == ft_strlen1(s2))
	{
		while (s1[i])
		{
			if (s1[i] - s2[i] != 0)
				return (-1);
			i++;
		}
	}
	return (0);
}
